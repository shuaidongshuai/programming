#include<stdio.h>
int main()
{
	int i,j,min,temp,a[10];
	printf("请输入：\n");
	for(i=0;i<10;i++)
	{
		printf("a[%d]=",i);
		scanf("%d",&a[i]);
	}
	printf("\n");
	printf("原始数据：");
	for(i=0;i<10;++i)
	{
		printf("%5d",a[i]); 
	}
	printf("\n重新排列：");
	for(i=0;i<9;i++)
	{
		min=i;
		for(j=i+1;j<10;j++)
			if(a[min]>a[j])
			{
				min=j;
			}
			temp=a[i];
		a[i]=a[min];
		a[min]=temp;
	}
	for(i=0;i<10;i++)
	{
		printf("%5d",a[i]);
	}
}
